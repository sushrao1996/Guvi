a=input("Enter the radius of the circle : ")

#Diameter
Diameter=2*float(a)
print("Diameter of the circle : " + str(Diameter))

#Circumference
Circumference=2*3.14*float(a)
print("Circumference of the circle : " +str(Circumference))

#Area
Area=3.14*float(a)*float(a)
print("Area of the circle : " + str(Area))
